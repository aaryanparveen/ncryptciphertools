def get_quadgram_score(text: str) -> float:
    _load_ngrams()
    if len(text) < 4:
        return -10.0
    score = 0.0
    n = 0
    for i in range(len(text) - 3):
        quad = text[i:i+4]
        score += _QUADGRAM_LOG.get(quad, _QUADGRAM_FLOOR)
        n += 1
    return score / max(1, n)


