import math

_QUADGRAM_LOG = {}
_QUADGRAM_FLOOR = -12.0
_BIGRAM_LOG_TABLE = {}
_BIGRAM_FLOOR = -10.0
_TRIGRAM_LOG = {}
_TRIGRAM_FLOOR = -11.0
_NGRAMS_LOADED = False


def _load_ngrams():
    global _QUADGRAM_LOG, _QUADGRAM_FLOOR
    global _BIGRAM_LOG_TABLE, _BIGRAM_FLOOR
    global _TRIGRAM_LOG, _TRIGRAM_FLOOR
    global _NGRAMS_LOADED

    if _NGRAMS_LOADED:
        return

    try:
        import cipheydists

        raw_quads = cipheydists.get_dist("quadgrams")
        total = sum(raw_quads.values())
        log_total = math.log10(total)
        _QUADGRAM_FLOOR = math.log10(0.01) - log_total
        for quad, count in raw_quads.items():
            _QUADGRAM_LOG[quad.upper()] = math.log10(count) - log_total

        raw_bigs = cipheydists.get_dist("bigrams")
        total_bi = sum(raw_bigs.values())
        log_total_bi = math.log10(total_bi)
        _BIGRAM_FLOOR = math.log10(0.01) - log_total_bi
        for bg, count in raw_bigs.items():
            _BIGRAM_LOG_TABLE[bg.upper()] = math.log10(count) - log_total_bi

        raw_tris = cipheydists.get_dist("trigrams")
        total_tri = sum(raw_tris.values())
        log_total_tri = math.log10(total_tri)
        _TRIGRAM_FLOOR = math.log10(0.01) - log_total_tri
        for tg, count in raw_tris.items():
            _TRIGRAM_LOG[tg.upper()] = math.log10(count) - log_total_tri

        _NGRAMS_LOADED = True
        print(f"[+] Loaded n-grams: {len(_QUADGRAM_LOG)} quadgrams, "
              f"{len(_BIGRAM_LOG_TABLE)} bigrams, {len(_TRIGRAM_LOG)} trigrams")

    except ImportError:
        print("[!] cipheydists not installed — using fallback n-gram data")
        _load_fallback_ngrams()
        _NGRAMS_LOADED = True


