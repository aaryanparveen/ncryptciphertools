_BIGRAM_LOG = None


def _init_bigram_log():
    global _BIGRAM_LOG
    if _BIGRAM_LOG is not None:
        return
    _BIGRAM_LOG = [[0.0]*26 for _ in range(26)]
    for r in range(26):
        for c in range(26):
            val = GUBALLA_BIGRAMS[r][c]
            _BIGRAM_LOG[r][c] = math.log(val) if val > 0 else -20.0


def get_bigram_log_score(c1: str, c2: str) -> float:
    _init_bigram_log()
    if not c1.isalpha() or not c2.isalpha():
        return -20.0
    r = ord(c1.upper()) - ord('A')
    c = ord(c2.upper()) - ord('A')
    if 0 <= r < 26 and 0 <= c < 26:
        return _BIGRAM_LOG[r][c]
    return -20.0


def score_bigram_fitness(text: str) -> float:
    _init_bigram_log()
    total = 0.0
    prev = -1
    for ch in text:
        if ch.isalpha():
            cur = ord(ch.upper()) - ord('A')
            if prev >= 0:
                total += _BIGRAM_LOG[prev][cur]
            prev = cur
    return total


def score_bigram_fitness_columns(col_a_indices, col_b_indices):
    _init_bigram_log()
    total = 0.0
    for a, b in zip(col_a_indices, col_b_indices):
        if 0 <= a < 26 and 0 <= b < 26:
            total += _BIGRAM_LOG[a][b]
    return total
